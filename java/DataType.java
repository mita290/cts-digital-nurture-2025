public class DataType {
    public static void main(String[] args) {
        int age = 25;
        float temperature = 36.6f;
        double pi = 3.1415926535;
        char grade = 'A';
        boolean booleanValue = true;

        System.out.println("Integer value (age): " + age);
        System.out.println("Float value (temperature): " + temperature);
        System.out.println("Double value (pi): " + pi);
        System.out.println("Character value (grade): " + grade);
        System.out.println("Boolean value (isJavaFun): " + booleanValue);
    }
}
